public class increament_decreament {
    public static void main(String[] args) {
        int i = 1;
        System.out.println(i);
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        System.out.println(i--);
        System.out.println(i);
        System.out.println(--i);
        System.out.println(i);
        System.out.println("\n\n");

        char ch = 'a';
        System.out.println(ch);
        System.out.println(ch++);
        System.out.println(ch);
        System.out.println(++ch);
        System.out.println(ch--);
        System.out.println(ch);
        System.out.println(--ch);
        System.out.println(ch);
    }
}
